<b>НОЧНАЯ ЖИЗНЬ</b><br>
Главное место встреч по вечерам, где можно насладиться очаровательными видами на лагуну и древнюю башню Киа в свете ночных огней - Piazza degli Ulivi. Здесь проводятся развлекательные мероприятия, шоу кабаре или концерты со специально приглашенными гостями. Продолжить ночную прогулку стоит в Luna Club, где можно выпить бокал вина, перекусить, послушать живую музыку и потанцевать на танцполе в саду. (Танцпол открыт в определенные выходные дни в высокий сезон).
<br><b>LUNA CLUB </b>– превосходное место для ночных прогулок под звездным небом. Любители живой музыки могут здесь перекусить, выпить и потанцевать (Танцпол открыт в определенные выходные дни в высокий сезон)  В распоряжении гостей - кафе-бар, открытый весь день, ресторан изысканной кухни, а также пиццерия, которая открывается по вечерам. Здесь также можно купить сэндвич во время ланча.
