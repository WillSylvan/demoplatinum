Опытные инструкторы Chia Laguna по дайвингу знают любой уголок таинственного подводного мира на южном побережье Сардинии и Киа. Это великолепный шанс взять несколько занятий по подводному плаванию со специальным снаряжением.
 <br><br>
<b>WATER SPORT SCENTRE</b><br>
Watersportscentreна пляже Dunedi Campana лучшее место для тех, чьей страстью является серфинг: здесь можно арендовать специальное снаряжение или взять несколько занятий с инструктором. Здесь есть все для виндсерфинга, катания на байдарках, лодке «банане» и обычных лодках.  
 <br><br>
<b>Is Molas Golf Club</b> - одно из самых известных полей для гольфа на Сардинии в 20-ти километрах от Киа. Здесь проводится Итальянский Международный Открытый турнир по гольфу.
 <br><br>
<b>ВЕЛОСИПЕДЫ</b><br>
Гости курорта могут арендовать городские и горные велосипеды или присоединиться к групповой велоэкскурсии по специальной программе.
