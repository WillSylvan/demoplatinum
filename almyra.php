					<p><b>Королевские гробницы</b></p>
					<p>
Эти впечатляющие своими размерами подземные гробницы датируются IV в. до н.э. Они высечены в массивных скалах и многие из них украшены дорическими колоннами. Подземные гробницы являлись прежде всего местом захоронения высокопоставленных жителей Пафоса, а также представителей местной аристократии. Они имели роскошное оформление, и потому получили свое название, хотя собственно королей здесь не хоронили.					
