Программа Baby Go Lightly (Baby-консьерж)
<br><br>
Теперь у родителей есть прекрасная возможность заказать все необходимое для ребенка в отеле Almyra заранее, до начала своего путешествия. Все, что требуется от родителей – это взять малыша с собой. Все остальные необходимые вещи для ухода за малышом будут ожидать Вас в номере по приезде.
<br><br>
От детского кресла в такси или на трансферах из аэропорта в отель до детских книжек на определенном языке и DVD дисков, от детских горшков, высоких детских стульев, подгузников, стерилизаторов для бутылочек, разнообразных бутылочек и сосок, до мобилей, «кенгуру», колясок, ходунков, банных принадлежностей (включая детский лосьон и присыпку), подогревателей для бутылочек, карандашей, раскрасок, детских влажных салфеток, нарукавников для плавания, детских сидений в туалет и детских ванночек для купания. Для нас нет ничего невозможного!
<br><br>
Baby Go Lightly
<br><br>
Дети в клубе разделяются на 5 возрастных групп:
<br><br>
1) Globetrotters: 4 месяца- 2 года 11месяцев
<br><br>
Наши опытные няни обсуждают с родителями все детали распорядка дня вашего малыша и исходя из этого планируют занятия для него. Это позволяет создать для ребенка наиболее комфортную обстановку, чтобы он оставался в своем привычном ритме и чувствовал себя счастливым.  Ежедневные занятия разработаны индивидуально с учетом погодных условий и, конечно, с учетом интересов малышей. В течение всего дня для малышей бесплатно предоставляются подгузники, плавательные подгузники,  свежее молоко, пюре и всегда свежеприготовленная еда. На время тихого часа малышей можно уложить спать в специально отведенной комнате для сна.
<br><br>
2) Adventurers: 3 года- 4 года 11месяцев
<br><br>
Для этой возрастной группы созданы множество самых интересных и творческих занятий: ребенок будет рад создать свою собственную мозаику, испечь вкуснейшие кипрские пирожные или участвовать в традиционных Кипрских играх в саду, организованных великолепной специально подготовленной  командой воспитателей детского клуба Explorers.
<br><br>
3) Voyagers: 5 лет- 7 лет 11 месяцев
<br><br>
Voyagers проводят практически все время (в зависимости от погодных условий) на открытом воздухе, принимая участие в увлекательных спортивных играх или всевозможных интересных занятиях, специально придуманных для них командой воспитателей. Элементы кипрской культуры и традиций вплетены в веселые интерактивные  игры, что позволяет детям лучше узнать эту удивительную страну, танцуя местные танцы или ухаживая за растущими в огороде отеля овощами. В свободное время дети могут поиграть со своими друзьями в специальной игровой зоне, где есть множествоигрушек и игр.
<br><br>
4) Pioneers: 8 лет- 12 лет 11 месяцев
<br><br>
Для этой возрастной группы супер активная команда воспитателей – так называемые Rangers разработала очень насыщенную спортивную и развлекательную программу. Большинство игр и развлечений проводятся на свежем воздухе на всей территории отеля.
<br><br>
5) Подростки: 13 лет +
<br><br>
Escape–это игровая зона, специально отведенная для подростков, оформленная в современном стилеграффити. Здесь размещаются столы для настольного тенниса  и пула, аэрохоккея, приставки Nintendo и дартс, а также предусмотрено специальное пространство для общения с друзьями и для совместного просмотра фильмов на большом экране. Специально разработанную программу развлечений и спортивных мероприятий проводит профессиональный ведущий из команды воспитателей Explorer. Программа может меняться и дополняться в зависимости от сферы интересов подростка.					
<br><br>
