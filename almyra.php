Виллы Диониса, Тесея, Эона и Орфеуса. Мозаичные полы этих построек, относящихся к III и V вв.  н.э. и принадлежащих выдающимся личностям своей эпохи, считаются одними из самых красивых в Восточном Средиземноморье. На них в основном изображены сцены из Греческой мифологии.					
