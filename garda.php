						<p><b>Музей MILLE MIGLIA</b></p>
						<p>
Несколько лет назад клуб Mille Miglia выкупил постройки бывшего бенедиктинского монастыря Святой Еуфемии, первое упоминание о котором датируется 1008 годом. Здесь открылся музей Mille Miglia, рассказывающий об истории знаменитых гонок на спортивных автомобилях. В залах музея представлены уникальные машины, костюмы гонщиков и фотографии с 1927 по 1957 год. История гонки Mille Miglia началась в 1927 году, когда удалось воплотить в жизнь идею необычной гонки на спортивных автомобилях. По замыслу организаторов, гонщики должны были сделать круг по перекрытым дорогам общего пользования — от Брешии до Рима и обратно. Прикинули маршрут: оказалось, что участники ралли должны преодолеть почти 1600 км. Вспомнив о том, что древние римляне измеряли расстояния в милях, организаторы решили обратиться к опыту предков и назвали гонку Mille Miglia — 1000 миль. Принять участие в современной гонке Mille Miglia может каждый. В соответствии с правилами, на старт легендарной гонки  могут выйти автомобили до 1957 года выпуска. Причем предпочтение отдается тем конкретным машинам, которые принимали участие в «оригинальных» гонках Mille Miglia — в период с 1927 по 1957 годы. И еще одно  важнейшее условие — аутентичность автомобиля. Замена «родных» узлов современными категорически запрещена. Конечно, нынешняя Mille Miglia — это уже не гонка на время, а состязание в пунктуальности. И — роскошное театрализованное представление.
						</p>
						
