						<p><b>Винодельческое хозяйство San Leonardo</b></p>
						<p>
enuta San Leonardo – знаменитое винодельческое хозяйство Италии, расположенное в живописных предгорьях Альп-Доломитов (область Трентино, северо-восток Италии). С XVIII века Tenuta San Leonardo принадлежит одной из самых аристократических семей севера Италии – маркизам Герьери Гонзага. Дружа с детства с маркизом Инчиза делла Рокетта (Sassicaia), Карло Герьери Гонзага нанял звездного энолога Сассикайи Джакомо Такиса (Giacomo Tachis), чтобы тот сделал похожее вино и у него. Так появилось вино San Leonardo – «бордосский» купаж каберне совиньона, мерло и каберне фран. Такис оставался энологом Сан Леонардо вплоть до 2002 года, когда его, ушедшего в отставку, заменил другой знаменитый специалист – Карло Феррини.
