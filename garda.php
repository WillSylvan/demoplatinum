Тренто – это небольшой, но очень красивый  город  на севере Италии. Этот город - ярчайшая иллюстрация переплетения итальянских и немецких культурных традиций. Перечислим лишь некоторые из достопримечательностей города: замок Кастелло-дель-Буонконсилио, в его стенах располагается Национальный музей, Кафедральный собор Св. Виджилио, с прилегающей к нему площадью Пьяцца-Дуомо с фонтаном в стиле неоклассицизма ”Нептун”, церковь Санта-Мария Маджоре (место проведения в 1562-63 гг. знаменитого Тридентского собора), дворец Палаццо-делле-Альбере, в котором сейчас находится музей современного искусства. Также стоит отметить железнодорожный вокзал - красивейший образец итальянского модернизма, многочисленные и не менее интересные музеи, расположенные вне исторических зданий – Аэронавтики, Альпинизма, Естественной истории, Фортепиано, Медной Утвари. В Тренто ежегодно проходит кинофестиваль, сельскохозяйственные ярмарки, музыкальные и исторические фестивали, а многочисленные магазины порадуют любителей шопинга сезонными распродажами. Живописные альпийские окрестности Тренто - настоящий подарок для любителей зимнего и летнего туризма. К их услугам лыжные трассы на любой вкус, пешие и велосипедные маршруты для походов и экскурсий.
