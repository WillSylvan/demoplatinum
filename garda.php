Огромный парк отеля заслуживает особого внимания – ведь это неотъемлемая часть курорта с конца 19 века. Площадь парка 12 500 квадратных метров, здесь можно найти разнообразные породы деревьев, в том числе кедры и кипарисы, магнолии и тисы, туи и пальмы.
