В SPA-центре отеля Grotta Giusti классические процедуры объединены с инновационными оздоровительными подходами и последними разработками в сфере эстетических услуг. В последние годы общей тенденцией становится появление в меню SPA-центров медицинских процедур, объединение эстетических уходов и восточных оздоровительных практик на основе средиземноморских SPA-традиций. 

В отеле следуют принципу SPA - salus per aquam – то есть здоровье через воду. Здесь важно восприятие всеми пятью чувствами, и влияние оказывается на всех уровнях –  на телесном,  на психологическом, и на душевном.

С древних времен люди использовали термальную воду для лечения от многих болезней. Возвращаясь к истокам, в Grotta Giusti используют силу термальной воды для оздоровления организма, восстановления внутреннего физического и  духовного баланса. Благодаря пристальному вниманию к мельчайшим деталям, пребывание в GrottaGiusti становится незабываемым в своем совершенстве SPA–опытом.
