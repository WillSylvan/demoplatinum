Жемчужина курорта - древний грот, разделенный на три зоны: Рай, Чистилище – с бассейном Лимб (с водой средней температуры), и Инферно, где влажность достигает 98%. В каждой зоне поддерживается разная температура, и чтобы расслабиться и получить максимум от терапевтических эффектов воды и ингаляций, можно совершить круг, который длится около 50 минут. Теперь вход в грот подсвечен таким образом, чтобы гости могли оценить естественную красоту этого уникального места, почувствовав его неповторимую атмосферу.

Исторический вход в SPA, примыкающий к гроту, был переделан в зону отдыха, декорированную изысканными материалами, а на словно выросшей из земли каменной стене красуются восхитительные переплетения цветов. Это новый SPA-центр, в котором гости могут читать, расслабляться или медитировать после купаний в бассейнах. 

Вода проходит через грот-солончак, обогащаясь солью, сульфатами и выходит из-под земли с температурой около 34°С. Испаряясь, она создает естественные пары, вдыхание которых лечит заболевания органов дыхания, кровообращения, гинекологические, опорно-двигательные, нервные и дерматологические расстройства. В SPA также предлагаются грязевые ванны, ингаляционные процедуры (аэрозоли и ингаляции).

Завсегдатаем грота был композитор Джузеппе Верди, который назвал его "восьмым чудом света". Антонио Тарджони Тоццетти, первый изучивший особенности находки фермеров, писал: "Воздух в этой пещере перемешивается с паром и, разогреваясь до оптимальной температуры, как показывает опыт, создает идеальное место для термальных купаний".
