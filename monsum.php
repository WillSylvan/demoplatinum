Гости могут попасть в SPA-центр не выходя из отеля: здесь к их услугам просторные помещения для традиционных процедур: грязевые обертывания, ингаляции и массаж, а также современный оздоровительный центр с ориентальной зоной, тренажерный зал, несколько помещений для отдыха, и центр эстетической медицины. А также природный грот и два открытых термальных бассейна: площадь одного из них, с гидромассажными струями и водопадом, составляет 750 кв метров, а второй, открытый в 2010 году рядом, представляет собой купальню с многочисленными круговыми струями, и небольшим уголком для отдыха, где в полной мере можно ощутить преимущества термальной воды из источников Grotta Giusti, богатой сульфатами, кальцием и магнием.

Не стоит забывать и о классических SPA-процедурах: грязелечение, озонаторные ванны в термальной воде, термальный душ, ингаляции – все под постоянным наблюдением врача-специалиста. Программы предназначены для восстановления здоровья, улучшения самочувствия и помогают вернуть  красоту.

В веллнесс-центре также предлагаются консультации диетологов, программы лечебного массажа, сочетающие восточные и западные традиции: шиатцу, дзен, аюрведа, широдхара, занятия фитнесом в тренажерном зале с личным тренером, аэробика, аква-аэробика, гимнастика, а также аквапилатес в термальной воде, где особое внимание уделяется мышцам пресса и пояснично-крестцовому отделу позвоночника – это позволяет повысить тонус и исправить осанку. Среди эстетических процедур для лица и тела – противовозрастные и антицеллюлитные, специальные программы для мужчин, услуги салона красоты, эстетическая медицина. В SPA используется косметическая линия Fonteverde – продукты для лица и тела, большинство из которых универсальны и  предназначены для ухода за всеми типами кожи. Маски и экстракты на основе термальной воды богаты эластином и коллагеном, а серия Grape Stem Cells на основе стволовых клеток винограда обладает мощным антиоксидантным эффектом. Кроме того, представлена также линия Comfort Zone.

Программа снижения веса в Grotta Giusti, созданная в 2011 году, основана на особом плане питания, разработанном специально медицинской командой курорта. Этот инновационный метод на стыке молекулярной биологии, генетики и превентивной медицины, был предложен профессором Джованни Скапанини, а куратором программы является д-р Никола Анжело Фортунати. После детальной диагностики, ряда анализов и генетических тестов составляется подробная карта пациента. На основании этих результатов подбирается персональная диета, а также рекомендуются наиболее подходящие виды терапии. Термальные процедуры - глина, бальнеотерапия и грот, которые, благодаря своей способности передавать тепло и стимулировать тем самым биологические реакции восстановления клеток, укрепляют иммунную систему – уникальная особенность программы Grotta Giusti. Более того, щелочные и нейтрализующие кислотность свойства Программы долголетия снижают риск воспалительных и дегенеративных процессов в организме.
