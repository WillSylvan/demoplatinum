15 номеров категории Prestige от 35 до 40 кв м<br>
5 номеров категории Deluxe от 30 до 34 кв м<br>
Большинство двухместных комнат могут быть объединены между собой. Во всех номерах - ванная комната с душевой кабиной и ванной.						
						
						
