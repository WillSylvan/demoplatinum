						<p>
						
L’Apogée Courchevel, расположенный на склонах французских Альп, стал седьмой жемчужиной Oetker Collection. Эксклюзивная коллекция объединяет роскошные отели по всему миру, отличительный чертой которых является сервис высочайшего уровня.
<br><br>
L’Apogée Courchevel – был построен по индивидуальному проекту на вершине престижного района Jardin Alpin, считающегося одним из самых красивых в Куршевеле 1850. Благодаря своему расположению, из окон отеля открывается панорамный вид на станцию и склоны. L’Apogée Courchevel имеет собственный прямой доступ ко всем трассам горнолыжного курорта.
<br><br>
Среди сотрудников гостиницы можно встретить много знакомых лиц, перешедших на работу в L’Apogée Courchevel из отелей du Cap-Eden-Roc и Château Saint-Martin & Spa. Таким образом, для гостей L’Apogée Courchevel будет обеспечено такое же высокое качество сервиса, которое существует в других отелях Коллекции.
<br><br>
Дизайн всех интерьеров принадлежит двум известным архитекторам - Индии Мадави и Жозефу Дирану. Они учли все для того, чтобы элегантность и стиль гармонично сочетались с комфортом и качеством.
<br><br>
Общий номерной фонд отеля насчитывает 56 комнат и апартаментов, включая Пентхаус и Шале. 
<br><br>
