						<p><b>Пентхаус</b></p>	
						<p>
Самый просторный Suite отеля L’Apogee Courchevel занимает последний этаж, откуда открывается панорамный вид на Куршевель. Пентхаус включает 4 спальные комнаты, три из которых могут быть подготовлены как для одноместного так и двухместного проживания. Такая конфигурация идеально подходит для отдыха с семьей или в кругу друзей. Просторные апартаменты так же включают салон и столовую. Гостям Пентхауса предоставляются услуги личного батлера.

Отличительная черта номера – большое количество балконов с красивейшими видами на горы. После дня катания на лыжах, гости апартаментов могут расслабиться в собственном мини спа Центре, включающем сауну, хаммам, джакузи, а так же джакузи на открытой террасе с панорамным видом на станцию. Так же в Пентхаусе для гостей оборудована отдельная комната для просмотра кинофильмов или игр на приставках последнего поколения.
