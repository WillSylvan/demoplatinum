Необыкновенно красивое и изящное здание Отеля du Cap было построено в 1870 году и относится к классическим образцам стиля эпохи Наполеона III. В начале, до того как стать в 1889 году легендарным отелем Grand Hotel du Cap, вилла получила имя " Villa Soleil " ("Вилла Солнца"). Одним из первых именитых гостей отеля стал Сэр Гордон-Беннетт - эксцентричный миллионер и владелец знаменитой газеты " New York Herald ". Вслед за ним Виллу посетила бесчисленная великосветская публика, включающая русских князей и английских пэров. Среди множества причин, сделавших известным это место отдыха мировой богемы безусловно нужно назвать имя принимавшего гостей молодого пьемонтца Антуана Селла…						
						
						
						
