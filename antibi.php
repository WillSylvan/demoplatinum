Историческое здание отеля. Построено в 1870 году. В здании 66 номеров - двухместные номера категории tradition и superior, junior suites и апартаменты.

Двухместные номера tradition и superior (38 номеров) расположены в различных частях здания отеля и обращены в сторону парка.
Декор всех комнат различен, однако, они все выдержаны в классическом дворцовом стиле эпохи Людовика XV и Людовика XVI. В оформлении номеров кроме антикварной мебели обращают на себя внимание подлинные произведения искусства прошлых веков. Общая площадь номера от 25 до 35 кв м. Ванная комната оформлена мрамором, раздельный туалет, душ, ванная, все необходимые косметические средства, тапочки, халаты, полотенца.

Junior Suites (20 номеров). Просторные номера с уголком - салоном площадью 40 м 2. Некоторые номера располагают отдельным салоном или двумя ванными комнатами. Из окон открывается вид на море, горный массив Эстерель (у некоторых номеров есть балкон) или же на парк и розарий отеля (первый этаж отеля, небольшая терраса или патио).
 
Апартаменты (8 номеров). Лучшие номера отеля располагают салоном и одной или двумя спальными комнатами. Из окон апартаментов открывается вид на море, Леринские острова или парк отеля. Общая площадь от 55 кв м.					
