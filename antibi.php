В здании 32 номера, включая Junior Suites и Suite Eden Roc
 Павильон Eden-Roc, построенный в 1914 году, изначально представлял собой Чайный Салон Grand Отеля Cap. В Салоне встречалась мировая аристократическая и культурная элита, ежегодно приезжавшая на отдых на мыс Антиб. Одним из его самых частых посетителей был Нобелевский лауреат 1921 года Анатоль Франс. После строительства бассейна, которые был устроен непосредственно в скале и десятка частных бунгало на берегу, которые быстро получили названия кабинок "Кабана", Чайный салон был переделан в ресторан отеля Eden Roc.
В 80-ых годах прошлого века на верхних этажах здания были созданы номера " Juniors suites ", а так же знаменитые апартаменты с видом на скалы мыса Антиб. С этого момента за возможность провести несколько дней в этом шикарном номере ежегодно борются Мадонна, Шэрон Стуон, Жак Ширак и многие другие знаменитости.
 
Junior Suites (31 номер). Номера категории "Juniors suites", площадью 40 кв м, располагают террасами с видом на море и просторными ванными комнатами. Декор комнат выдержан в традиционном для отеля классическом стиле. Исключительный комфорт и роскошь!
 
Suite Eden Roc. Самые шикарные апартаменты располагают просторной комнатой, салоном, двумя ванными комнатами, гардеробной комнатой и террасой впечатляющих размеров с фонтаном. Салон и спальная, примыкающие к террасе имеют огромные раздвижные окна-двери с панорамным видом на море. В случае необходимости к сюиту может быть присоединен соседний номер категории junior suite.
Площадь номера - 120 кв (возможно присоединение номера junior suite - 40 кв метров)
Площадь террасы - 160 кв м
Общая площадь - 280 кв м
