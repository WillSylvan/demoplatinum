					<p><b>1/ Обзорная экскурсия по Зальцбургу</b></p>

					<p>Экскурсия включает посещение Дворца и парка Mirabell, построенного архиепископом для своей возлюбленной Саломеи Альт, кладбища St. Sebastian, где похоронены Парацельс и родители Моцарта, Дома Моцарта, где композитор жил с 1773 по 1780 годы, главной пешеходной улицы старого Зальцбурга - Гетрайдегассе (Getreidegasse), дома, где родился Моцарт - небольшой дом-музей Гетрайдегассе, Кафедрального собора - сооружение в стиле раннего барокко, свидетельство 1000 летней власти архиепископского двора, крепости Hohensalzbug, откуда открывается невероятный вид на город, монастыря St. Peter и Кладбища - с этого монастыря, основанного в 696 году, началась история города, а живописное кладбище - самое древнее в Австрии.
