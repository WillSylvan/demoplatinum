					<p><b>2/ Дворец и парк Hellbrunn</b></p>

					<p>Достоин посещения дворец Hellbrunn, построенный по указу архиепископа Sittacus и находящийся примерно в восьми километрах от Зальцбурга, служил также охотничьей резиденцией.
					<br>
					Парк Hellbrunn украшен скульптурами и фонтанами, среди которых - "шутихи", известные нам по Петергофу. Рассказывают, что любящий повеселиться архиепископ сервировал гостям столы в парке, и, когда они достигали определенной степени опьянения, включались спрятанные механизмы, и на гостей низвергались потоки воды.
					</p>
					<p><b>3/ Соляные копи Зальцбурга, город Халляйн</b></p>
					<p>
					Соль, добываемая в недрах горы Дюренберг, дала имя не только земле и городу Зальцбург. Со времени кельтских поселений до периода правления зальбургских князей-архиепископов соль обеспечивала благосостояние княжества. Экскурсия одинаково интересна для взрослых и детей. Вы услышите историю о знаменитом "Соляном человеке" и насладитесь неповторимым миром природы, посмотрите фильм, познакомитесь с миром восковых фигур, прокатитесь на горках и шахтерских вагонетках, проплывете на лодке по подземному озеру под звуки органа. Экскурсия включает посещение городка Халляйн.
