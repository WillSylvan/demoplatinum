						История отеля Gleneagles 
						<br>Gleneagles был открыт в 1924 году и был описан международными экспертами, как "Ривьера в Высокогорье" и как "Восьмое чудо Света". Отель был построен компанией Caledonian Railway Company. Теперь курорт принадлежит компании Diageo, которая является производителем таких брэндов виски, как Bell's, Johnnie Walker, J&B, White Horse.
						 
						<br><br>К 1930 году курорт с великолепными гольф-полями стал уже невероятно популярным. Здесь собирались представители королевских фамилий, дипломаты, президенты, премьер-министры, известные мировые звезды и самые знаменитые гольфисты мира. До 1982 года отель был открыт только в летний период. С 1982 по 1986 года в реставрацию отеля было вложено более 11 миллионов фунтов стерлингов. В результате реконструкции Gleneagles был усовершенствован и обновлен. С этого момента гости могли наслаждаться многочисленными возможностями отеля круглый год. С 1986 года было инвестировано более 46 миллионов фунтов стерлингов. Gleneagles сохранил неповторимый стиль, лучшие традиции шотландского дружелюбия и гостеприимства, и по-прежнему получает международные награды и самые высокие оценки и экспертов со всего мира.



