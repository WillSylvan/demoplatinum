<br><b>Гольф в Gleneagles</b>

<br>Гольф и Gleneagles - неразделимые понятия. Сегодня курорт располагает 3 гольф-полями (PGA Centenary Course, Queen's Course, King's Course) и широко известен в кругах как профессиональных, так и начинающих гольфистов.
<br>В момент открытия отеля, в 1924 году, на территории Gleneagles уже существовали два гольф поля - King's Course и Queen's Course. Их создателем является такая мировая звезда, как Джеймс Брейд, пятикратный призер открытого чемпионата по гольфу. Эти поля являются ярким примером "классического" гольф-дизайна. 
 

<br><b>Школа охотничьих собак</b>
<br>Одной из последних новинок в отеле Gleneagles является открытие здесь школы охотничьих собак. Школа, не имеющая мировых аналогов, будет добавлена к списку различных возможностей для активного отдыха.
  
<br><b>Верховая езда</b>
<br>В пяти минутах ходьбы от основного здания отеля расположена школа верховой. Школа Gleneagles считается одной из самых лучших в мире. В ней профессиональные инструкторы дадут уроки для начинающих и проведут занятия по выездке или конкуру. Уроки могут проходить и в закрытых манежах, и на открытых площадках. Для продвинутых ездоков школа может устроить соревнования.
<br>Гости отеля могут также совершить прогулку по окрестностям. Инструкторы посоветуют, какой именно маршрут выбрать. Все зависит от того, как ездок держится в седле. Здесь есть маршруты различной сложности: для начинающих, для любителей проехаться галопом и преодолевать препятствия. Самым маленьким наездникам предложат дружелюбных и прекрасно обученных пони, покататься на которых мечтают все дети.
<br>На территории школы расположены 2 крытых обогреваемых манежа, один из которых предназначен для представлений и вмещает до 830 зрителей, а также большой открытый тренировочный плац и конюшни. Помимо этого в распоряжении гостей имеется магазин со всеми необходимыми аксессуарами для верховой езды и ресторан.

 
<br><b>Рыбалка</b>
<br>Во время пребывания в Gleneagles любители рыбалки могут отправиться на одно из трех озер на территории отеля, на реку Тай и попробовать поймать форель. Под руководством специалиста из рыболовной школы отеля, знающего особенности вод и обитающих рыб, рыбалка может стать прекрасным отдыхом. Сезон для рыбалки - круглый год. По шотландским законам, в воскресенье рыбная ловля запрещена.
 
<br><b>Стрельба</b>
<br>Школа Gleneagles имеет репутацию лучшей школы стрельбы по любым дисциплинам. Специалисты проводят уроки для стрелков всех уровней. Гости специально приезжают в Gleneagles со всего мира, чтобы проверить свои навыки в стрельбе и подготовится к охоте. Стрельба из ружья на некоторых этапах имитирует охоту на птиц, обитающих в болотистой местности.
<br>Школа использует знаменитые ружья компании Beretta. Специальная одежда, транспорт для передвижения от одного этапа к другому, ружья предоставляются школой и включены в стоимость урока. Все необходимые аксессуары для стрельбы, сменная одежда могут быть куплены в магазинчике, расположенном недалеко от этапов. Здесь же можно устроить пикник. Гостиная домика достаточно просторная и вмещает до 25 человек. 
 
<br><b>Соколиная охота</b>
<br>Соколиная охота - один из самых старинных и аристократичных видов спорта. На территории курорта расположена Британская школа по соколиной охоте, в которой инструкторы расскажут гостям, как обращаться с птицами, а также дадут несколько уроков по соколиной охоте.
<br>Специалисты с удовольствием подробно расскажут о вековых традициях охоты и о характере каждой птицы. После того, как гость наденет защитную одежду и перчатку, его посвятят в таинства управления соколами. А как только гость приобретет некоторые навыки, он сможет отправиться на урок в места, которые могли бы подойти и для охоты.
 
  <br><b>Езда на внедорожниках</b>
<br>Проехаться по бездорожью на мощной машине мечтают все. В Gleneagles в распоряжении гостей - последние модели внедорожников Land Rover, которые будут пробираться по болотистой местности, преодолевать гористые участки, проезжать бурлистые реки. Уроки с профессиональными инструкторами могут быть организованы как для нескольких гостей, так и для группы.

 
<br><b>Теннис </b>
<br>На территории курорта в распоряжении гостей 5 открытых теннисных корта с искусственными покрытиями и 4 новых крытых теннисных корта Arena с акриловым покрытием.
 
<br><b>SPA</b>
