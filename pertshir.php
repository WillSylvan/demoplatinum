<br>2 закрытых и открытый бассейн, закрытый гидромассажный бассейн, тренажерный зал, студия для проведения тренировок по аэробике, паровая комната и сауны, комнаты для отдыха SPA-бар.
<br>Салон красоты для дам, включая маникюр и педикюр. Парикмахерский салон для мужчин, включая услуги цирюльника.
<br>К услугам гостей 20 SPA-кабинок, где делают процедуры с использованием косметики ESPA.
